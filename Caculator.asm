org 100h

.DATA
    ; operator stack
    OP_STACK_SIZE EQU 50
    OP_STACK DB OP_STACK_SIZE DUP(?)
    OP_STACK_PTR DW 0  ; operator stack pointer

    ; operand stack
    VAL_STACK_SIZE EQU 50
    VAL_STACK DW VAL_STACK_SIZE DUP(?) 
    VAL_STACK_PTR DW 0  ; operand stack pointer 
    
    ; buffer for input string
    BUFFER_SIZE DB 51
    ACTUAL_LEN  DB ?
    INPUT_STR   DB 50 DUP(?)
    
    ; COUNTER FOR READING THE STRING 
    COUNTER DB 0
    
    ; a charecter for new line( for print the result )
    NEWLINE     DB 0Dh, 0Ah, '$'  
    
    ; OUTPUT MASSAGE AND OUTPUT BUFFER
    RESULT_MSG  DB 'RESULT = $'
    OUTPUT_BUF  DB 6 DUP('$')   
    
    ; A MASSAGE FOR INVALID CHARECTER
    ERROR_INVALID_CHAR_MSG DB 'Error: Invalid Character Found!$' 
    
    ; A MASSAGE FOR DIV BY ZERO
    DIV_ERR_MSG DB 'Error: Division by zero is undefined!$'  
    
    ; A MASSAGE FOR UNBALANCED PAREN
    PAREN_ERR_MSG DB 'Error: Unbalanced Parentheses!$'
    
.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    ; --- get input ---
    CALL GET_INPUT 
    
    ; --- CONVERT INFIX INTO POSTFIX ---
    CALL PROCESS_EXPRESSION    
    
    ; --- CACULATE FINALL RESULT ---
    CALL FINALIZE_CALCULATION

    ; --- PRINT NEW CHARECTER FOR PRINT RESULT IN NEXT LINE ---
    CALL PRINT_NEW_LINE 
                        
    ; --- PRINT THE RESULT ---
    CALL PRINT_RESULT                    
                        
    ; --- PRINT THE INPUT ( FOR TEST ) ---
    ;CALL PRINT_STRING 
    
MAIN ENDP 

; ---FUNCTIONS---    

;1. --- get input ---
GET_INPUT PROC
    LEA DX, BUFFER_SIZE    ; DX = BUFFER ADDRESS 
    MOV AH, 0Ah            
    INT 21h                ; INTRUPT 
    RET
GET_INPUT ENDP  

;2. --- PRINT NEW LINE ---
PRINT_NEW_LINE PROC
    LEA DX, NEWLINE
    MOV AH, 09h
    INT 21h
    RET
PRINT_NEW_LINE ENDP  

